// Language utilities
let currentLang = localStorage.getItem('siteLang') || 'en';

function getBrowserLang() {
    const navLang = navigator.language || navigator.userLanguage;
    if (navLang && navLang.startsWith('ar')) return 'ar';
    return 'en';
}

async function detectCountryLang() {
    try {
        const res = await fetch('https://ipapi.co/json/');
        const data = await res.json();
        const arCountries = ['SA', 'AE', 'EG', 'QA', 'KW', 'BH', 'OM', 'JO', 'LB', 'IQ', 'SY', 'YE', 'PS', 'SD', 'LY', 'TN', 'DZ', 'MA', 'MR'];
        if (data && data.country_code && arCountries.includes(data.country_code)) {
            return 'ar';
        }
    } catch (e) {
        console.log('IP detection failed, falling back to browser language');
    }
    return getBrowserLang();
}

async function initLanguage() {
    const saved = localStorage.getItem('siteLang');
    if (!saved) {
        const detected = await detectCountryLang();
        currentLang = detected;
        localStorage.setItem('siteLang', currentLang);
    } else {
        currentLang = saved;
    }
    applyLanguage(currentLang);
}

function applyLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('siteLang', lang);
    document.documentElement.lang = lang === 'ar' ? 'ar' : 'en';
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.body.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');

    // Update all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const text = getTranslation(key, lang);
        if (text) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = text;
            } else {
                el.textContent = text;
            }
        }
    });

    // Update lang buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Re-render dynamic content
    if (typeof loadHomePage === 'function') {
        loadHomePage();
    }
}

function getTranslation(key, lang) {
    const dict = translations[lang] || translations.en;
    return dict[key] || translations.en[key] || key;
}

function setLanguage(lang) {
    applyLanguage(lang);
}

// Toggle mobile menu
function toggleMobileMenu() {
    const menu = document.getElementById('mobileMenu');
    menu.classList.toggle('show');
}

// Generate star rating HTML
function generateStars(rating) {
    let html = '';
    for (let i = 1; i <= 5; i++) {
        if (i <= rating) {
            html += '<span class="star">★</span>';
        } else {
            html += '<span class="star empty">★</span>';
        }
    }
    return html;
}

// Render review card
function renderReviewCard(review) {
    const featuredBadge = currentLang === 'ar' ? 'مميز' : 'FEATURED';
    const featuredClass = review.featured ? 'featured' : '';
    const avatarHtml = review.avatar
        ? `<img src="${review.avatar}" alt="${review.reviewerName}" class="reviewer-avatar-img">`
        : `<div class="reviewer-avatar">${review.reviewerName.charAt(0)}</div>`;

    return `
        <div class="review-card ${featuredClass}">
            <div class="review-header">
                <div class="reviewer-info">
                    ${avatarHtml}
                    <div>
                        <div class="reviewer-name">${review.reviewerName} <span>${review.age} • ${review.location}</span></div>
                        <div class="review-rating">${generateStars(review.rating)}</div>
                    </div>
                </div>
            </div>
            <div class="review-content">"${review.content}"</div>
            <span class="effect-tag">${review.effect}</span>
            <div class="review-meta">
                <span>📅 ${review.date}</span>
                <span>⏱️ ${review.usageTime}</span>
            </div>
        </div>
    `;
}

// Load homepage data
function loadHomePage() {
    const lang = currentLang;
    // Load stats
    const stats = DataManager.getStats(lang);
    const totalReviewsEl = document.getElementById('totalReviews');
    const verifiedClientsEl = document.getElementById('verifiedClients');
    const successRateEl = document.getElementById('successRate');

    if (totalReviewsEl) totalReviewsEl.textContent = stats.totalReviews;
    if (verifiedClientsEl) verifiedClientsEl.textContent = stats.verifiedClients;
    if (successRateEl) successRateEl.textContent = stats.successRate + '%';

    // Load reviews
    const featuredReviews = DataManager.getFeaturedReviews(40, lang);
    const reviewsContainer = document.getElementById('featuredReviews');

    if (reviewsContainer) {
        if (featuredReviews.length === 0) {
            reviewsContainer.innerHTML = `
                <div class="empty-state" style="grid-column: 1 / -1;">
                    <div class="empty-state-icon">💬</div>
                    <h3>${getTranslation('loading', lang)}</h3>
                </div>
            `;
        } else {
            reviewsContainer.innerHTML = featuredReviews.map(r => renderReviewCard(r)).join('');
        }
    }
}

// Run when page loads
document.addEventListener('DOMContentLoaded', async function() {
    DataManager.initData();
    await initLanguage();

    if (document.getElementById('featuredReviews')) {
        loadHomePage();
    }
});
